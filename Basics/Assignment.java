/*
Assignment Operators
=   	x = 5	    x = 5	
+=	    x += 3	    x = x + 3	
-=	    x -= 3	    x = x - 3	
*=	    x *= 3	    x = x * 3	
/=	    x /= 3	    x = x / 3	
%=	    x %= 3	    x = x % 3	
&=	    x &= 3	    x = x & 3	
|=	    x |= 3	    x = x | 3	
^=	    x ^= 3	    x = x ^ 3	
>>=	    x >>= 3	    x = x >> 3	
<<=	    x <<= 3	    x = x << 3	

 */
import java.lang.*;

public class Assignment{
    public static void main(String args[]){
        int a=10;
        System.out.println(a);
        a+=10;
        System.out.println(a);
        a-=10;
        System.out.println(a);
        a/=2;
        System.out.println(a);
        
    }
}